function getImageUrl(chapter, season)
{
    let basePath = 	"https://gitlab.com/a94jfmcl3V3js4na5lvslfuj38I/mv4/-/raw/master/Anime/";
    let URLSeason = "0" + season + "/";
    let URLChapter = getChapterFormat(chapter.id) + ".png" ;

    return basePath + URLSeason + URLChapter;
}

function getChapterFormat(chapterId) 
{
    if (chapterId > 0 && chapterId < 10)
        return "00" + chapterId;
    else if (chapterId > 9 && chapterId < 100)
        return "0" + chapterId;
    else
        return chapterId;
}